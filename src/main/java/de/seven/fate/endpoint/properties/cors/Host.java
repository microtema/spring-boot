package de.seven.fate.endpoint.properties.cors;

import javax.validation.constraints.NotNull;

public class Host {

    @NotNull
    private String ip;
    private int port;

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public int getPort() {
        return port;
    }

    public void setPort(int port) {
        this.port = port;
    }
}